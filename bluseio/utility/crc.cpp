uint16_t crc16(const void* p, size_t n, uint16_t crc) {
    const uint8_t* s = (const uint8_t*) p;
    while (n--) {
        crc = (crc << 8) ^ crc16tab[((crc >> 8) ^ *s++) & 0x00ff];
    }
    return crc;
}

uint32_t crc32(uint32_t crc, const uint8_t *buffer, uint32_t size) {
	uint32_t i = 0;

	for (i = 0; i < size; i++) {
		crc = crc32table[((crc>>24) ^ buffer[i]) & 0xff] ^ (crc << 8);  
	}  
	return crc ;  
}
